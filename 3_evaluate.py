"""
=============================================================
AEGIS PROTOCOL — Evaluation Runner  (3_evaluate.py)
=============================================================
Runs THREE solvers on all 7 discriminating test cases:
  naive  — worst-case baseline  (linear scan, no reuse)
  yours  — your smart hybrid solver
  oracle — offline Smin estimator (full future knowledge)

Validates every output geometrically.
Computes Steam_naive, Steam_yours, Smin, Ratio, Score/5, Time.
Prints a full formatted 3-way comparison report.

Prerequisites:
    python3 gen_discriminating_tests.py   (creates ./tests/)
    ./2_build_solvers.sh  or  2_build_solvers.bat

Usage:
    python3 3_evaluate.py

Optional flags:
    --your-solver  PATH   default: ./bin/aegis  (or aegis.exe on Windows)
    --oracle       PATH   default: ./bin/oracle
    --naive        PATH   default: ./bin/naive
    --tests-dir    PATH   default: ./tests
    --time-limit   SECS   default: 30.0
    --max-pts      FLOAT  default: 5.0

Understanding Smin:
    Smin = oracle activations (offline, reads ALL future disks upfront).
    Oracle uses: score = 0.7 * norm_future_freq + 0.3 * centrality
    True competition Smin = lowest Steam among all teams (unknown until judging).
    Oracle Smin <= true Smin  =>  your real competition score >= shown here.
=============================================================
"""

