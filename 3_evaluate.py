# ─── Subprocess runner ────────────────────────────────────────────────────────
def run_binary(path, input_data, time_limit):
    """
    Pipe input_data into binary at `path`.
    Returns (stdout | None, elapsed_secs, error_msg | None).
    """
    try:
        t0     = time.time()
        result = subprocess.run(
            [path],
            input=input_data,
            capture_output=True,
            text=True,
            timeout=time_limit,
        )
        elapsed = time.time() - t0
        if result.returncode != 0:
            msg = result.stderr.strip().replace("\n", " ")[:120]
            return None, elapsed, f"Exit {result.returncode}: {msg}"
        return result.stdout, elapsed, None
    except subprocess.TimeoutExpired:
        return None, time_limit, "TLE"
    except FileNotFoundError:
        return None, 0.0, f"Binary not found: {path}"
    except Exception as exc:
        return None, 0.0, str(exc)


# ─── Geometric validator ──────────────────────────────────────────────────────
def validate(input_data, output_str):
    """
    Parse input and output together and verify every response:
      1. Output index is an integer in [0, N).
      2. The chosen satellite lies inside or on the boundary of its disk.

    Returns (valid: bool, steam: int, error_msg: str).
    steam = number of unique satellite indices activated.
    """
    lines = input_data.strip().split("\n")
    ptr   = 0

    N = int(lines[ptr]); ptr += 1
    pts = []
    for _ in range(N):
        x, y = map(float, lines[ptr].split()); ptr += 1
        pts.append((x, y))

    Q = int(lines[ptr]); ptr += 1
    disks = []
    for _ in range(Q):
        cx, cy, r = map(float, lines[ptr].split()); ptr += 1
        disks.append((cx, cy, r))

    out = [ln.strip() for ln in output_str.strip().split("\n") if ln.strip()]

    if len(out) != Q:
        return False, 0, f"Expected {Q} output lines, got {len(out)}"

    activated = set()
    for q in range(Q):
        try:
            idx = int(out[q])
        except ValueError:
            return False, 0, f"Query {q+1}: non-integer '{out[q]}'"

        if not (0 <= idx < N):
            return False, 0, f"Query {q+1}: index {idx} out of [0, {N})"

        cx, cy, r = disks[q]
        sx, sy    = pts[idx]
        dist2     = (sx - cx) ** 2 + (sy - cy) ** 2
        if dist2 > r * r + 1e-6:
            dist = dist2 ** 0.5
            return (
                False, 0,
                f"Query {q+1}: sat {idx} at ({sx:.2f},{sy:.2f}) is OUTSIDE "
                f"disk center=({cx:.2f},{cy:.2f}) r={r:.2f} "
                f"[dist={dist:.4f} > r={r:.4f}]"
            )
        activated.add(idx)

    return True, len(activated), "OK"


# ─── Convenience wrapper ──────────────────────────────────────────────────────
def run_and_validate(binary, input_data):
    """
    Run binary and validate output.
    Returns (steam | None, elapsed, status_tag).
    status_tag: "OK" | "TLE" | "WA:<reason>"
    """
    out, t, err = run_binary(binary, input_data, TIME_LIMIT)
    if err:
        tag = "TLE" if err == "TLE" else f"WA:{err[:50]}"
        return None, t, tag
    valid, steam, msg = validate(input_data, out)
    if not valid:
        return None, t, f"WA:{msg[:50]}"
    return steam, t, "OK"


# ─── Preflight ────────────────────────────────────────────────────────────────
def preflight():
    """Check that all binaries and the test directory exist."""
    ok = True
    binaries = [
        ("Your solver", YOUR_SOLVER),
        ("Oracle",      ORACLE),
        ("Naive",       NAIVE),
    ]
    for label, path in binaries:
        if not os.path.isfile(path):
            print(f"  ERROR: {label} binary not found at '{path}'")
            ok = False
        elif not IS_WINDOWS and not os.access(path, os.X_OK):
            print(f"  ERROR: {label} binary at '{path}' is not executable")
            ok = False
    if not os.path.isdir(TESTS_DIR):
        print(f"  ERROR: Tests directory not found: '{TESTS_DIR}'")
        ok = False
    return ok


# ─── Main ─────────────────────────────────────────────────────────────────────
def main():
    W = 100   # total line width

    print()
    print("=" * W)
    print("  AEGIS PROTOCOL — 3-Way Evaluation Report")
    print(f"  Naive  : {NAIVE}")
    print(f"  Yours  : {YOUR_SOLVER}")
    print(f"  Oracle : {ORACLE}  (Smin estimator — hybrid future-freq + centrality)")
    print(f"  Limit  : {TIME_LIMIT}s per case  |  Max pts/case : {MAX_PTS}")
    print("=" * W)

    if not preflight():
        print("\n  Fix the above errors and re-run.\n")
        sys.exit(1)

    # ── Column header ─────────────────────────────────────────────────────────
    print()
    print(
        f"{'#':<3} {'Test Case':<32} "
        f"{'Naive':>6} {'Yours':>6} {'Smin':>6}  "
        f"{'R_naive':>7} {'R_yours':>7}  "
        f"{'Sc_naive':>8} {'Sc_yours':>8}  "
        f"{'Improv':>7}  "
        f"{'T_naive':>7} {'T_yours':>7}  "
        f"Status"
    )
    print("-" * W)

    # ── Accumulators ──────────────────────────────────────────────────────────
    tot_score_naive = 0.0
    tot_score_yours = 0.0
    tot_max         = 0.0
    n_valid         = 0
    n_wa            = 0
    n_tle           = 0

    for tc_num, (name, fname, _why) in enumerate(TEST_CASES, start=1):
        fpath     = os.path.join(TESTS_DIR, fname)
        tot_max  += MAX_PTS

        # ── Load ──────────────────────────────────────────────────────────────
        if not os.path.isfile(fpath):
            print(f"{tc_num:<3} {name:<32} {'MISSING FILE':>70}")
            n_wa += 1
            continue

        with open(fpath, encoding="utf-8") as f:
            data = f.read()

        # ── Run all three solvers ──────────────────────────────────────────────
        steam_naive,  t_naive,  st_naive  = run_and_validate(NAIVE,       data)
        steam_yours,  t_yours,  st_yours  = run_and_validate(YOUR_SOLVER, data)
        steam_oracle, _t_oracle, st_oracle = run_and_validate(ORACLE,     data)

        # ── Determine Smin ────────────────────────────────────────────────────
        # Smin = oracle's Steam (offline lower bound).
        # If oracle failed, fall back to the better of naive/yours.
        if steam_oracle is not None:
            smin = steam_oracle
        else:
            candidates = [s for s in [steam_naive, steam_yours] if s is not None]
            smin = min(candidates) if candidates else 0

        # Safety clamp: Smin must never exceed what any valid solver achieved.
        # (The oracle may still be slightly above on edge cases; we correct here.)
        valid_steams = [s for s in [steam_naive, steam_yours] if s is not None]
        if valid_steams:
            smin = min(smin, min(valid_steams))

        # ── Score both solvers ────────────────────────────────────────────────
        def score_of(steam):
            if steam is None:
                return 0.0, 0.0
            ratio = smin / max(steam, 1)
            return ratio, ratio * MAX_PTS

        ratio_naive, sc_naive = score_of(steam_naive)
        ratio_yours, sc_yours = score_of(steam_yours)

        if steam_naive is not None:
            tot_score_naive += sc_naive
        if steam_yours is not None:
            tot_score_yours += sc_yours

        # ── Count outcomes ────────────────────────────────────────────────────
        any_fail = (steam_naive is None or steam_yours is None)
        if not any_fail:
            n_valid += 1
        else:
            for st in [st_naive, st_yours]:
                if st == "TLE":
                    n_tle += 1
                elif st.startswith("WA"):
                    n_wa += 1

        # ── Improvement: fewer activations by yours vs naive (%) ──────────────
        if steam_naive and steam_yours:
            improv = (steam_naive - steam_yours) / max(steam_naive, 1) * 100.0
        else:
            improv = 0.0

        # ── Status badge ──────────────────────────────────────────────────────
        if   st_yours != "OK" and st_naive != "OK":
            badge = "BOTH FAIL"
        elif st_yours != "OK":
            badge = f"YOURS:{st_yours}"
        elif st_naive != "OK":
            badge = f"NAIVE:{st_naive}"
        else:
            bar_n = "█" * int(round(ratio_naive * 5)) + "░" * (5 - int(round(ratio_naive * 5)))
            bar_y = "█" * int(round(ratio_yours * 5)) + "░" * (5 - int(round(ratio_yours * 5)))
            badge = f"N[{bar_n}] Y[{bar_y}]"

        # ── Format helpers ────────────────────────────────────────────────────
        def fmt_int(v):   return f"{v:>6}"   if v is not None else f"{'---':>6}"
        def fmt_float(v): return f"{v:>7.4f}" if v is not None else f"{'---':>7}"

        print(
            f"{tc_num:<3} {name:<32} "
            f"{fmt_int(steam_naive)} {fmt_int(steam_yours)} {fmt_int(smin)}  "
            f"{fmt_float(ratio_naive)} {fmt_float(ratio_yours)}  "
            f"{sc_naive:>7.3f}/{MAX_PTS:.0f} {sc_yours:>7.3f}/{MAX_PTS:.0f}  "
            f"{improv:>+6.1f}%  "
            f"{t_naive:>6.2f}s {t_yours:>6.2f}s  "
            f"{badge}"
        )

    # ── Summary ───────────────────────────────────────────────────────────────
    n_cases    = len(TEST_CASES)
    pct_naive  = tot_score_naive / tot_max * 100 if tot_max else 0.0
    pct_yours  = tot_score_yours / tot_max * 100 if tot_max else 0.0
    pct_gain   = pct_yours - pct_naive

    print("=" * W)
    print(f"  {'Cases run':<45}  {n_cases}  ({n_valid} valid / {n_wa} WA / {n_tle} TLE)")
    print(f"  {'Naive  total score':<45}  {tot_score_naive:6.3f} / {tot_max:.1f}  "
          f"({pct_naive:.2f}%)")
    print(f"  {'Yours  total score':<45}  {tot_score_yours:6.3f} / {tot_max:.1f}  "
          f"({pct_yours:.2f}%)")
    print(f"  {'Score improvement  (yours - naive)':<45}  "
          f"{tot_score_yours - tot_score_naive:+.3f} pts  ({pct_gain:+.2f} pp)")
    print("=" * W)

    # ── Smin explanation ──────────────────────────────────────────────────────
    print()
    print("  UNDERSTANDING Smin")
    print("  " + "-" * (W - 2))
    print("  Smin shown = oracle activations (reads ALL Q disks before answering).")
    print("  Oracle scoring: score = 0.7 × norm_future_freq + 0.3 × centrality")
    print("    norm_future_freq  = freq[i] / max_freq  (how often sat i appears in ANY disk)")
    print("    centrality        = (R - dist) / R       (how deep sat i is inside THIS disk)")
    print()
    print("  In the real competition:")
    print("    true Smin  =  minimum Steam across ALL submitted teams  (unknown until judging)")
    print("    true Smin  >=  oracle Smin  (no online solver can beat an offline oracle)")
    print("    => your real competition ratio  >=  ratio shown here")
    print("    => your real competition score  >=  self-eval score shown here")
    print()
    print("  The naive ratio shows what score a student gets with zero heuristic effort.")
    print("  The gap between naive and yours is the measurable value of your strategy.")
    print("=" * W)

    # ── Per-test why-discriminating notes ─────────────────────────────────────
    print()
    print("  WHY EACH TEST CASE IS DISCRIMINATING")
    print("  " + "-" * (W - 2))
    for tc_num, (name, _fname, why) in enumerate(TEST_CASES, start=1):
        print(f"  TC{tc_num:02d}  {name:<30}  {why}")
    print()

    # ── Advice if yours < naive (shouldn't happen) ───────────────────────────
    if tot_score_yours < tot_score_naive - 0.1:
        print("  WARNING: Your solver scored BELOW naive on these discriminating cases.")
        print("  Possible causes:")
        print("    1. ALPHA/BETA weights in aegis_protocol.cpp are mis-tuned.")
        print("    2. Density precomputation is not converging (cap too low).")
        print("    3. Active-satellite reuse check is not firing correctly.")
        print()


if __name__ == "__main__":
    main()